    @Test
    @Transactional
    public void createCompany() throws Exception {
        int databaseSizeBeforeCreate = companyRepository.findAll().size();

        // Create the Company
        restCompanyMockMvc.perform(post("/api/companies")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(company)))
            .andExpect(status().isCreated());

        // Validate the Company in the database
        List<Company> companyList = companyRepository.findAll();
        assertThat(companyList).hasSize(databaseSizeBeforeCreate + 1);
        Company testCompany = companyList.get(companyList.size() - 1);
        assertThat(testCompany.getCode()).isEqualTo(DEFAULT_CODE);
        assertThat(testCompany.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testCompany.getImage()).isEqualTo(DEFAULT_IMAGE);
        assertThat(testCompany.getImageContentType()).isEqualTo(DEFAULT_IMAGE_CONTENT_TYPE);
        assertThat(testCompany.getTel()).isEqualTo(DEFAULT_TEL);
        assertThat(testCompany.getEmail()).isEqualTo(DEFAULT_EMAIL);
        assertThat(testCompany.getOmNumber()).isEqualTo(DEFAULT_OM_NUMBER);
        assertThat(testCompany.getType()).isEqualTo(DEFAULT_TYPE);
        assertThat(testCompany.getMomoNumber()).isEqualTo(DEFAULT_MOMO_NUMBER);
        assertThat(testCompany.getSystemCommision()).isEqualTo(DEFAULT_SYSTEM_COMMISION);

        // Validate the Company in Elasticsearch
        Company companyEs = companySearchRepository.findOne(testCompany.getId());
        assertThat(companyEs).isEqualToIgnoringGivenFields(testCompany);
    }

