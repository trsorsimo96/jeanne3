    @Test
    @Transactional
    public void updateCompany() throws Exception {
        // Initialize the database
        companyRepository.saveAndFlush(company);
        companySearchRepository.save(company);
        int databaseSizeBeforeUpdate = companyRepository.findAll().size();

        // Update the company
        Company updatedCompany = companyRepository.findOne(company.getId());
        // Disconnect from session so that the updates on updatedCompany are not directly saved in db
        em.detach(updatedCompany);
        updatedCompany
            .code(UPDATED_CODE)
            .name(UPDATED_NAME)
            .image(UPDATED_IMAGE)
            .imageContentType(UPDATED_IMAGE_CONTENT_TYPE)
            .tel(UPDATED_TEL)
            .email(UPDATED_EMAIL)
            .omNumber(UPDATED_OM_NUMBER)
            .type(UPDATED_TYPE)
            .momoNumber(UPDATED_MOMO_NUMBER)
            .systemCommision(UPDATED_SYSTEM_COMMISION);

        restCompanyMockMvc.perform(put("/api/companies")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedCompany)))
            .andExpect(status().isOk());

        // Validate the Company in the database
        List<Company> companyList = companyRepository.findAll();
        assertThat(companyList).hasSize(databaseSizeBeforeUpdate);
        Company testCompany = companyList.get(companyList.size() - 1);
        assertThat(testCompany.getCode()).isEqualTo(UPDATED_CODE);
        assertThat(testCompany.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testCompany.getImage()).isEqualTo(UPDATED_IMAGE);
        assertThat(testCompany.getImageContentType()).isEqualTo(UPDATED_IMAGE_CONTENT_TYPE);
        assertThat(testCompany.getTel()).isEqualTo(UPDATED_TEL);
        assertThat(testCompany.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testCompany.getOmNumber()).isEqualTo(UPDATED_OM_NUMBER);
        assertThat(testCompany.getType()).isEqualTo(UPDATED_TYPE);
        assertThat(testCompany.getMomoNumber()).isEqualTo(UPDATED_MOMO_NUMBER);
        assertThat(testCompany.getSystemCommision()).isEqualTo(UPDATED_SYSTEM_COMMISION);

        // Validate the Company in Elasticsearch
        Company companyEs = companySearchRepository.findOne(testCompany.getId());
        assertThat(companyEs).isEqualToIgnoringGivenFields(testCompany);
    }

